#本节要理解b=a,b=copy.copy(a),b=copy.deepcopy(a),也就是赋值，浅复制，深复制之间的区别
#1：赋值：（可以用画一个圆圈，圆圈代表位置，里面可以储存元素，圆圈上贴着a,b两个标签，虽然标签名字不同，但是位置id相同）
'''
a=[1,2,3]
b=a
print(id(a))
print(id(b))
#由上可知，相当于同一个盒子，上面有a和b两个标签，名字不同但是储存路径相同，所以id相同
print(id(a[0]))
print(id(b[0]))
#同理，两个标签下的元素id也相同
b[1]=555
print(a)
print(b)
#改变其中一个元素，另一个标签下的元素也会改变
'''
#2:内嵌列表下的理解
a=[1,2,3,[4,5]]#注意元素3后面也要有逗号
b=a
print(id(a[3][0]))
print(id(b[3][0]))
#可以发现赋值后内嵌列表的元素id也相同
a[3][0]=666
print(a)
print(b)
#内嵌列表中的元素改变，另一个标签下的相同位置元素也会改变
print(id(a[3][0]))
print(id(b[3][0]))
#内嵌列表中的元素改变后，两个标签下被改变元素的位置id仍然相同
